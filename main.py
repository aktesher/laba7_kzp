def create_empty_matrix(size):
    """
    Створює порожню матрицю розміру size x size, заповнену пробілами.
    """
    matrix = []  # Створюємо порожній список для матриці
    row = [' '] * size  # Створюємо один рядок із пробілами

    # Додаємо копії цього рядка до матриці
    for _ in range(size):
        matrix.append(row.copy())  # Додаємо копію рядка в матрицю

    return matrix


def inner_fill(matrix, symbol):
    """
    Заповнює внутрішність матриці символом, залишаючи контур незаповненим.
    """
    size = len(matrix)
    for i in range(1, size - 1):  # Починаємо з другого рядка і закінчуємо перед останнім
        for j in range(1, size - 1):  # Починаємо з другого стовпця і закінчуємо перед останнім
            matrix[i][j] = symbol  # Заповнюємо внутрішні елементи символом


# Виводить матрицю на екран поелементно.
def display_matrix(matrix):
    """
    Виводить матрицю на екран.
    """
    for row in matrix:
        for symbol in row:
            print(symbol, end=' ')
        print()  # Перехід на новий рядок після кожного рядка матриці

# INT MAIN {}
size = int(input("Введіть розмір квадратної матриці: "))
symbol = input("Введіть символ для контуру: ")

# Перевірка на додатний розмір матриці
if size <= 0:
    print("Розмір матриці має бути додатнім числом!")
# Перевірка на введення лише одного символу
elif len(symbol) != 1:
    print("Помилка: введіть лише один символ для заповнення контуру!")
else:
    # Створюємо порожню матрицю та заповнюємо її контур
    matrix = create_empty_matrix(size)
    inner_fill(matrix, symbol)
    
    # Виводимо матрицю на екран
    display_matrix(matrix)


